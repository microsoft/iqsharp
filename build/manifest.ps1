<#
    .SYNOPSIS
        Provides the list of artifacts (Packages and Assemblies) generated by this repository.
    
    .PARAMETER OutputFormat
        Specifies if the output of this script should be a hashtable with the artifacts
        as strings with the absolute path (AbsolutePath) or FileInfo structures.
#>
param(
    [ValidateSet('FileInfo','AbsolutePath')]
    [string] $OutputFormat = 'FileInfo'
);


& "$PSScriptRoot/set-env.ps1"

$artifacts = @{
    Packages = @(
        "Microsoft.Quantum.IQSharp.Core",
        "Microsoft.Quantum.IQSharp.ExecutionPathTracer",
        "Microsoft.Quantum.IQSharp.Jupyter",
        "Microsoft.Quantum.IQSharp"
    ) | ForEach-Object { Join-Path $Env:NUGET_OUTDIR "$_.$Env:NUGET_VERSION.nupkg" };

    Assemblies = @(
        "./src/AzureClient/bin/$Env:BUILD_CONFIGURATION/netstandard2.1/Microsoft.Quantum.IQSharp.AzureClient.dll",
        "./src/Core/bin/$Env:BUILD_CONFIGURATION/netstandard2.1/Microsoft.Quantum.IQSharp.Core.dll",
        "./src/ExecutionPathTracer/bin/$Env:BUILD_CONFIGURATION/netstandard2.1/Microsoft.Quantum.IQSharp.ExecutionPathTracer.dll",
        "./src/Jupyter/bin/$Env:BUILD_CONFIGURATION/netstandard2.1/Microsoft.Quantum.IQSharp.Jupyter.dll",
        "./src/Kernel/bin/$Env:BUILD_CONFIGURATION/netstandard2.1/Microsoft.Quantum.IQSharp.Kernel.dll",
        "./src/Tool/bin/$Env:BUILD_CONFIGURATION/net6.0/Microsoft.Quantum.IQSharp.dll",
        "./src/Web/bin/$Env:BUILD_CONFIGURATION/netcoreapp3.1/Microsoft.Quantum.IQSharp.Web.dll"
    ) | ForEach-Object { Join-Path $PSScriptRoot (Join-Path ".." $_) };
} 

if ($OutputFormat -eq 'FileInfo') {
    $artifacts.Packages = $artifacts.Packages | ForEach-Object { Get-Item $_ };
    $artifacts.Assemblies = $artifacts.Assemblies | ForEach-Object { Get-Item $_ };
}

$artifacts | Write-Output;
